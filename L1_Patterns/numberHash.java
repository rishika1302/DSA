package Patterns;

import java.util.*;
public class numberHash {
    public static void main(String[] args) throws Exception{
        Scanner scn = new Scanner(System.in);
        int n = scn.nextInt();
        for(int i=0; i<n; i++){
            for(int j=0; j<n; j++){
                System.out.print("4");
            }
            System.out.println();
        }
        scn.close();
    }
}
